<h2>Travel Tips....</h2><br><br>
</center>
<h3>1.When it comes to traveling abroad, first piece of document you need is a passport. It is the most important document.</h3><br>
<h3>2.If you pulled over or stopped by the law enforcement officers in a foreign country, passport is you first identity along with your international drivers license.</h3><br>
<h3>3.Before you travel, make sure you passport is valid and will not be expire while you are away. Most of the country will not accept your passport if it has expired.</h3><br>
<h3>4.Always take good care of your passport. Don't give or handover your passport to anyone. You need that to go and come in every country of the world. Without a passport you will not be able to get in the plane.</h3><br>
<h3>5.Keep your passport with you at all times. If you are visiting friends or family, make sure that your passport is in a safe place. If you are in a hotel or shopping around the town, always check on it.</h3><br>
<h3>6.Make at least 3 photo copies of your passport. Leave one behind at your permanent address, 2nd copy with you when you traveling but not with the original passport. In case you lose your passport, you would need the photocopy for your identification.</h3><br>
<h3>7.If you have a scanner-scan you passport and send it to your own email address. It could be very helpful in case if you lose your passport and photocopy can't be located.</h3><br>
<h3>8.If incase, you lost your passport, notify the local police authority and contact your embassy immediately. Do not delay on reporting the lost of your passport.</h3><br>
<h3>9.Make sure you have the proper health/immunization requirements.</h3><br>
<h3>10.Carry as much currency in travelers checks as possible.</h3><br>
<h3>11.Keep a copy of all documents and credit card information in a safe place.</h3><br>
<h3>12.Find out some information about your destination. It is to your advantage to know some basic phrases in the local language, basic facts, places of interest, how to get around etc.</h3><br>
<h3>13.Check all your travel documents to make sure that they are in order. Passports should be valid for a few months after your return date.</h3><br>
<h3>14.Bring your address and telephone book. Make sure you have jotted down important telephone numbers in case of emergency.</h3><br>
<h3>15.Check the weather conditions of your destination.</h3><br>
<h3>16.Become very alert when you are pushed around or distracted in a crowd.</h3><br>
<h3>17.When staying at a hotel, have your keys ready before you reach your room.</h3><br>
<h3>18.Every time you are in your hotel room, you use the double lock at all times.</h3><br>
<h3>19.If any of your important travel documents such as passport are lost or stolen, immediately notify the local police. </h3><br>
<h3>20.If you know that your country has an embassy in that country, report this to them and they can help you process temporary travel documents. </h3><br>
<h3>21.For credit cards make sure you call the issuing bank immediately so that they can stop the card from being used illegally.</h3><br>

